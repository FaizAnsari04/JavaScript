let Month = 1;
var Result;
switch (Month) {
    case 1:
        Result: console.log(`january`);
        break;
    case 2:
        Result: console.log("February");
        break;

    case 3:
        Result: console.log("March");
        break;
    case 4:
        Result: console.log("April");
        break;
    case 5:
        Result: console.log("May");
        break;
    case 6:
        Result: console.log("June");
        break;
    case 7:
        Result: console.log("July");
        break;
    case 8:
        Result: console.log("August");
        break;
    case 9:
        Result: console.log("September");
        break;
    case 10:
        Result: console.log("October");
        break;
    case 11:
        Result: console.log("November");
        break;
    case 12:
        Result: console.log("December");
        break;

    default:
        console.log("Not A Month");
        break;
}